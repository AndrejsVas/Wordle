package com.bootcamp.wordle.model;

public class Guess {
    private int id;
    private String word;

    public Guess() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
